// generate-package-contexts.mjs
// BrandOS Per-Package Context Generator
//
// Generates .context/packages/<slug>.generated.md for every package.
//
// Authority sources:
//   1. scripts/shared/package-registry.mjs  (layer, build order)
//   2. packages/<name>/package.json         (declared dependencies)
//   3. packages/<name>/src/index.ts         (public exports)
//   4. packages/<name>/AGENT_CONTEXT.md     (existing agent notes, verbatim)
//   5. check-boundaries.mjs                 (ownership and restriction rules)
//
// Usage: node scripts/generate-package-contexts.mjs

import {
  LAYER_TIERS, LAYER_INDEX, KNOWN_PACKAGES, BUILD_ORDER,
} from './shared/package-registry.mjs';
import {
  readJsonSafe, readFileSafe, ensureDir, extractPublicExports,
  getBrandosDeps, renderTimestamp,
} from './shared/context-utils.mjs';
import {
  CPL_BI_ALLOWED_SYMBOLS, PACKAGE_RESTRICTIONS, CPL_PROXY_SURFACE,
} from './shared/package-restrictions.mjs';
import { join, resolve, relative } from 'path';
import { writeFileSync } from 'fs';
import { fileURLToPath } from 'url';

const __dirname  = fileURLToPath(new URL('.', import.meta.url));
const ROOT       = resolve(join(__dirname, '..'));
const OUT_DIR    = join(ROOT, '.context', 'packages');


// ── Build reverse-dependent map ───────────────────────────────────────────────

function buildInventory() {
  return KNOWN_PACKAGES.map(({ name, dir, app }) => {
    const pkg  = readJsonSafe(join(ROOT, dir, 'package.json')) ?? {};
    const deps = getBrandosDeps(pkg);
    const exps = app ? [] : extractPublicExports(join(ROOT, dir));
    return { name, dir, app: !!app, layerIndex: LAYER_INDEX[name] ?? '?', deps, exps };
  });
}

function buildDependentMap(inventory) {
  const map = Object.fromEntries(inventory.map(({ name }) => [name, []]));
  for (const { name, deps } of inventory)
    for (const dep of deps)
      if (map[dep]) map[dep].push(name);
  return map;
}

// ── Render single package context ─────────────────────────────────────────────

function renderPackage({ name, dir, app, layerIndex, deps, exps }, dependentMap) {
  const slug         = name.replace('@brandos/', '');
  const layerLabel   = `L${layerIndex}`;
  const tier         = LAYER_TIERS[layerIndex] ?? [];
  const tierLabel    = tier.map(n => n.replace('@brandos/', '')).join(', ');
  const dependents   = dependentMap[name] ?? [];
  const restrictions = PACKAGE_RESTRICTIONS[name] ?? [];

  // Existing AGENT_CONTEXT.md  -  include as a section if present
  const agentCtx = readFileSafe(join(ROOT, dir, 'AGENT_CONTEXT.md')).trim();

  const lines = [
    `# \`${name}\` — Package Context (Generated)\n`,
    `> **Generated:** ${renderTimestamp()}`,
    '> ⚠️ Do not edit — regenerated by `scripts/generate-package-contexts.mjs`\n',
    '---\n',

    `## Identity\n`,
    `| Field | Value |`,
    `|---|---|`,
    `| Package | \`${name}\` |`,
    `| Location | \`${dir}\` |`,
    `| Layer | ${layerLabel} — ${tierLabel} |`,
    `| App (non-library) | ${app ? 'Yes' : 'No'} |`,
    `| Build order index | ${BUILD_ORDER.indexOf(name) + 1} of ${BUILD_ORDER.length} |`,
    '',

    `## Dependencies\n`,
    deps.length
      ? deps.map(d => `- \`${d}\``).join('\n')
      : '*No @brandos/* dependencies — foundational package*',
    '',

    `## Dependents (packages that import this)\n`,
    dependents.length
      ? dependents.map(d => `- \`${d}\``).join('\n')
      : '*No known dependents — top of stack*',
    '',
  ];

  // Public exports
  if (!app && exps.length > 0) {
    lines.push(`## Public Exports\n`);
    lines.push(`Entry point: \`${dir}/src/index.ts\`\n`);
    const rows = [];
    for (let i = 0; i < exps.length; i += 5)
      rows.push(exps.slice(i, i + 5).map(e => `\`${e}\``).join(', '));
    lines.push(rows.join(',\n'));
    lines.push('');
  }

  // Architectural restrictions
  if (restrictions.length > 0) {
    lines.push(`## Architectural Restrictions\n`);
    for (const r of restrictions) lines.push(`- ${r}`);
    lines.push('');
  }

  // CPL proxy table for apps/web
  if (name === '@brandos/web') {
    lines.push(CPL_PROXY_SURFACE);
  }

  // Key integration points (layer-specific)
  lines.push(`## Key Integration Points\n`);
  if (name === '@brandos/control-plane-layer') {
    lines.push(`- **Entry point for apps/web:** \`runControlPlane(request)\``);
    lines.push(`- **Brand memory proxies:** \`src/brand-memory/service.ts\``);
    lines.push(`- **Orchestrator:** \`CPLOrchestrator.orchestrate()\``);
    lines.push(`- **Artifact pipeline:** \`executeArtifactPipeline()\` → \`globalArtifactEngine.compileAndGovern()\``);
  } else if (name === '@brandos/brand-intelligence') {
    lines.push(`- **Runtime entry:** \`initBrandIntelligenceRuntime()\` / \`getGlobalBrandIntelligenceRuntime()\``);
    lines.push(`- **Factories:** \`createDegradedCognitionContext()\`, \`createBrandSignalRepository()\``);
    lines.push(`- **V2 tables:** \`brand_memory_entries\`, \`identity_signals\`, \`identity_versions\``);
  } else if (name === '@brandos/output-control-layer') {
    lines.push(`- **Pre-generation:** \`ContractAssemblerFactory.create()\` → \`compilePromptFromContract()\``);
    lines.push(`- **Post-generation:** \`normalizeOutput()\` → \`compile*Artifact()\` → \`ArtifactV2\``);
    lines.push(`- **PersonaContributor:** fully self-contained (WS3 — no BI delegation)`);
  } else if (name === '@brandos/governance-layer') {
    lines.push(`- **Text scoring:** \`evaluateGovernance(input)\``);
    lines.push(`- **Structured validation:** \`validateCarouselArtifact()\`, \`validateDeckArtifact()\`, \`validateReportArtifact()\``);
    lines.push(`- **Semantic governance:** \`runCarouselSemanticGovernance()\`, \`runDeckSemanticGovernance()\`, \`runReportSemanticGovernance()\``);
  } else if (name === '@brandos/artifact-engine-layer') {
    lines.push(`- **Singleton:** \`globalArtifactEngine\` (from \`bootstrap.ts\`) — use this, don't construct ArtifactEngine directly`);
    lines.push(`- **Pipeline:** \`engine.compileAndGovern()\` — compile → govern → repair (max 2 attempts) → export`);
    lines.push(`- **No-touch files:** \`engine.ts\`, \`registry.ts\``);
  } else if (name === '@brandos/auth') {
    lines.push(`- **Single source of truth** for Supabase auth and all DB CRUD`);
    lines.push(`- **React context:** \`AuthProvider\`, \`useAuth()\``);
    lines.push(`- **DB ops:** all getUserBy*, getWorkspace*, getCampaigns*, getPersonas* etc.`);
  } else if (name === '@brandos/ai-runtime-layer') {
    lines.push(`- **Primary call surface:** \`callWithMode(mode, prompt)\` — never throws`);
    lines.push(`- **Config wiring:** \`setRuntimeConfigProvider()\` — call once at startup in CPL`);
    lines.push(`- **Bridge:** \`globalThis.__brandos_runtime_adapter\` for artifact prompt injection`);
  } else if (name === '@brandos/presentation-layer') {
    lines.push(`- **Auth bridge:** \`PLAuthProvider\` / \`IPLAuthContext\` — wraps \`useAuth()\` into shell context`);
    lines.push(`- **Shell components:** \`WorkspaceShell\`, \`AdminShell\` (use \`usePLAuth()\` internally)`);
    lines.push(`- **Renderers:** \`CarouselRenderer\`, \`DeckRenderer\`, \`ReportRenderer\`, \`RendererRegistry\``);
  } else if (name === '@brandos/contracts') {
    lines.push(`- **Structural constraints:** \`CAROUSEL_STRUCTURAL_CONSTRAINTS\`, \`DECK_STRUCTURAL_CONSTRAINTS\`, \`REPORT_STRUCTURAL_CONSTRAINTS\``);
    lines.push(`- **Zero runtime deps** — safe to import from any layer`);
    lines.push(`- **Brand cognition V2:** \`IBrandCognitionRequest\`, \`IObservationEvent\`, \`SignalClassification\``);
  } else if (name === '@brandos/governance-config') {
    lines.push(`- **Re-exports structural constraints** from \`@brandos/contracts\` with \`AssertEqual\` drift detection`);
    lines.push(`- **Pass threshold:** \`DEFAULT_PASS_THRESHOLD = 65\``);
    lines.push(`- **Approval threshold:** \`DEFAULT_APPROVAL_SCORE_THRESHOLD = 70\``);
  }
  lines.push('');

  // Existing AGENT_CONTEXT.md (verbatim, clearly labelled)
  if (agentCtx) {
    lines.push(`## Existing AGENT_CONTEXT.md (hand-authored)\n`);
    lines.push(`> This section is the original hand-authored agent context. It is included verbatim.`);
    lines.push(`> Generated sections above supersede it where they conflict.\n`);
    lines.push(agentCtx);
    lines.push('');
  }

  return lines.join('\n');
}

// ── Main ──────────────────────────────────────────────────────────────────────

function main() {
  console.log('[generate-package-contexts] Starting…');
  ensureDir(OUT_DIR);

  const inventory    = buildInventory();
  const dependentMap = buildDependentMap(inventory);
  let written = 0;

  for (const entry of inventory) {
    const slug    = entry.name.replace('@brandos/', '').replace('/', '-');
    const outFile = join(OUT_DIR, `${slug}.generated.md`);
    const content = renderPackage(entry, dependentMap);
    writeFileSync(outFile, content);
    written++;
    console.log(`  ✅ ${relative(ROOT, outFile)}`);
  }

  console.log(`[generate-package-contexts] ✅ ${written} package context files written`);
}

main();
